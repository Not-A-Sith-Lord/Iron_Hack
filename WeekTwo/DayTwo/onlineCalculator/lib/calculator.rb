class Calculator

def addition(first, second)
  
  result = first + second
  "#{first} + #{second} = #{result}"
end

def subtraction(first, second)
 
  result = first - second
  "#{first} - #{second} = #{result}"
end

def multiplication (first, second)
  
  result = first * second
  "#{first} * #{second} = #{result}"
end

def division(first, second)
  
  result = first / second
  "#{first} / #{second} = #{result}"
end

end